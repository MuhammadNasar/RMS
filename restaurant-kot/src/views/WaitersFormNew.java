    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (txtUpdateWaiterName.getText().equals("") || txtUpdateFatherName.getText().equals("")
                || txtUpdateWaiterNumber.getText().equals("") || txtUpdatePhoneNumber.getText().equals("")
                || txtUpdateCnic.getText().equals("Cnic") || txtUpdateAddress.getText().equals("")
                || txtUpdateWaiterName.getText().equals("") || txtUpdateFatherName.getText().equals("")
                || txtUpdateWaiterNumber.getText().equals("") || txtUpdatePhoneNumber.getText().equals("")
                || txtUpdateCnic.getText().equals("") || txtUpdateCnic.getText().equals("")) {

            JOptionPane.showMessageDialog(rootPane, "empty data can not be updated");
            //  JOptionPane.showMessageDialog(rootPane, "please insert data");
            System.out.println(txtWaiterName.getText());

        } else {
            waiters = new Waiters();
            int waiterId = Integer.parseInt(txtUpdateId.getText().trim());
            waiters.setWaiterId(waiterId);
            waiters.setName(txtUpdateWaiterName.getText().trim());
            waiters.setFatherName(txtUpdateFatherName.getText().trim());
            waiters.setAddress(txtUpdateAddress.getText().trim());
            waiters.setWaiterNumber(txtUpdateWaiterNumber.getText().trim());
            waiters.setPhoneNumber(txtUpdatePhoneNumber.getText().trim());
            waiters.setCnic(txtUpdateCnic.getText().trim());

            waiterService.updateWaiters(waiters);
            //JOptionPane.showMessageDialog(rootPane, "data Upadated successfully");

        }

        vectorWaiter = commonService.getVectorWaiters();
        WaitersTableModel waitersTableModel = new WaitersTableModel(vectorWaiter);
        tbleWaiter.setModel(waitersTableModel);
    }//GEN-LAST:event_btnUpdateActionPerformed

